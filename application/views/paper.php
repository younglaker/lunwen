		<h2 class="paper-sub-title">论文简介：</h2>
		<div class="paper-intro"> 航天飞行器以高超声速再入大气层时，由于高温高压以及与大气的摩擦会在飞行器的表面形成等离子鞘套，严重影响飞行器与外界通信。针对电磁波在高速飞行器等离子鞘衰减问题，利用低气压辉光放电原理在地面产生大面积、长时间稳定持续的可控等离子体，需要确定等离子体产生装置所产生等离子体电子密度分布情况及提高电子密度方法，本文主要研究在实验室现有条件下的低气压辉光放电等离子体模拟与特性研究。文章对辉光放电理论、带电粒子在放电气体中的运动与碰撞进行深入分析，确定影响等离子体特性的因素。然后运用流体模型，将低温放电等离子体粒子作为流体来处理，数学模型方程由分别描述电子、离子的连续性方程和传输方程以及描述电场分布的泊松方程组成。最后根据实验室等离子体产生装置的条件，利用COMSOLMultiphysics仿真软件实现了低气压辉光放电等离子体模拟，分析了所产生的等离子体的相关特性，并与实验结果进行对比，结果表明等离子体产生装置能够产生大面积近似均匀的等离子体，通过改变阴极材料增加二次电子发射系数和提高功率的方法可以有效提高等离子体电子密度。 </div>
